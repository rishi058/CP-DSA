vector<string> all;

void solve(int open, int close, int n, string curr){
    if(curr.size()==2*n){
        all.push_back(curr);
        return ;
    }

    if(open>=close){
        if(open!=n){
            curr.push_back('{');
            solve(open+1, close, n, curr);
            curr.pop_back();
        }
        curr.push_back('}');
        solve(open, close+1, n, curr);
        curr.pop_back();
    }

}

