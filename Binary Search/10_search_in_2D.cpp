bool searchMatrix(vector<vector<int>>& v, int x) {
    int row = v.size();
    int col = v[0].size();
    
    int lo=0, hi=row-1;
    while(lo<=hi){
        int mid = (lo+hi)/2;
        if(v[mid][0]<=x){lo=mid+1;}
        else{hi=mid-1;}
    }
    if(lo!=0){lo--;}
    return binary_search(v[lo].begin(), v[lo].end(), x);
}

