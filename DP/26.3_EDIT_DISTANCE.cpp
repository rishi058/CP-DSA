int minDistance(string s1, string s2) {
    
    s1 = '0'+s1;
    s2 = '0'+s2;

    int n = s1.size(), m = s2.size();

    int dp[n][m];
    
    for(int i=0; i<n; i++){
        for(int j=0; j<m; j++){
            if(i+j==0){dp[i][j]=0;}
            else if(i==0){dp[i][j] = dp[i][j-1]+1;}
            else if(j==0){dp[i][j] = dp[i-1][j]+1;}
            else if(s1[i]==s2[j]){
                dp[i][j] = dp[i-1][j-1];
            }
            else{
                dp[i][j] = min({dp[i-1][j], dp[i][j-1], dp[i-1][j-1]})+1;
            }
        }
    }

    return dp[n-1][m-1]; 
}

