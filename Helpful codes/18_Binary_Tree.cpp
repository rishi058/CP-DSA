/*
There are 5 types of Binary tree(0 or 1 or 2 child):

1.) Full/ proper/ strict Binary tree ->  node must contain either 0 or 2 children/each node must contain 2 children except the leaf nodes.

2.) Complete Binary tree -> nodes are completely filled except the last level. In the last level, all the nodes is on left(side) as possible.

3.) Perfect Binary tree -> A tree is a perfect binary tree if all the internal nodes have 2 children, and all the leaf nodes are at the same level.

4.) Degenerate(skewed) Binary tree -> The degenerate binary tree is a tree in which all the internal nodes have only one children.

Balanced Binary tree -> The balanced binary tree is a tree in which both the left and right trees differ by atmost 1. 
                        For example, AVL and Red-Black trees are balanced binary tree.
*/